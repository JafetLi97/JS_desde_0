// tipos de datos primitivos

// los tipos de datos primitivos son: number, string, boolean, null, undefined, symbol. estos tipos de datos son inmutables, es decir, no se 
// pueden cambiar.

// en primer lugar, numeros

let numero = 1;
console.log(numero); // 1

// en segundo lugar, cadenas

let cadena = 'hola';
console.log(cadena); // hola

// en tercer lugar, booleanos\

let booleano = true;
console.log(booleano); // true

// en cuarto lugar, null

let nulo = null;
console.log(nulo); // null

// en quinto lugar, undefined

let indefinido;
console.log(indefinido); // undefined

let undef = undefined;
console.log(undef); // undefined. puede ser un tipo de dato o un valor. en este caso es un tipo de dato.







 
