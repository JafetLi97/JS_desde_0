// tipado estatico y tipado dinamico

// en javascript existe el tipado dinamico, es decir, no es necesario declarar el tipo de dato de una variable. el tipo de dato se asigna
// automaticamente segun el valor que se le asigne a la variable. en javascript, una variable puede cambiar de tipo de dato en cualquier
// momento. esto se llama tipado dinamico.



let numero = 1;
let nombre = "chanchito feliz";
let booleano = true;
let undef = undefined;
let nulo = null;


console.log(typeof nombre); 
console.log(typeof numero); 
console.log(typeof booleano); 
console.log(typeof undef); 
console.log(typeof nulo); 