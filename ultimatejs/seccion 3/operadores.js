// operadores en JS

// los operadores en JS son simbolos que se utilizan para realizar operaciones matematicas, logicas, de comparacion, de asignacion, etc. en JS los
// operadores se utilizan para realizar operaciones con valores y variables. los operadores en JS se utilizan para realizar operaciones de suma,
// resta, multiplicacion, division, modulo, incremento, decremento, comparacion, logica, etc.

// tipos de operadores en JS

/*
Aritmeticos: se utilizan para realizar operaciones matematicas con valores numericos. los operadores aritmeticos en JS son +, -, *, /, %, ++, --.
Asignacion: se utilizan para asignar valores a variables. los operadores de asignacion en JS son =, +=, -=, *=, /=, %=.
Comparacion: se utilizan para comparar dos valores. los operadores de comparacion en JS son ==, ===, !=, !==, >, <, >=, <=.
Logicos: se utilizan para realizar operaciones logicas con valores booleanos. los operadores logicos en JS son &&, ||, !.
bitwise: se utilizan para realizar operaciones bitwise con valores numericos. los operadores bitwise en JS son &, |, ^, ~, <<, >>, >>>.

*/